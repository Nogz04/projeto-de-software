 public void preencherComboComidas() {

        ComidaDAO cDAO = new ComidaDAO();
        List<Comida> listaComidas = cDAO.getComidas();
        for (Comida c : listaComidas) {
            cmbComidas.addItem(c);
        }

    }
 
  public void preencherComboBebidas() {

        BebidaDAO bDAO = new BebidaDAO();
        List<Bebida> listaBebidas = bDAO.getBebidas();
        for (Bebida b : listaBebidas) {
            cmbBebidas.addItem(b);
        }

    }
  
    public void preencherComboSobremesas() {

        SobremesaDAO sDAO = new SobremesaDAO();
        List<Sobremesa> listaSobremesas = sDAO.getSobremesas();
        for (Sobremesa s : listaSobremesas) {
            cmbSobremesas.addItem(s);
        }

    }
    
