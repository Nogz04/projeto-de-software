    private void btnEnviarParaCarrinhoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarParaCarrinhoActionPerformed
        
            Pedido p = new Pedido();
            
            Comida comidaSelecionada = (Comida) cmbComidas.getSelectedItem();
            p.setId_comida(comidaSelecionada);
            p.setQuantidade_comida(Integer.parseInt(txtQuantidadeComida.getText()));
            p.setValor_comida(p.getId_comida().getValor());
            
            Bebida bebidaSelecionada = (Bebida) cmbBebidas.getSelectedItem();
            p.setId_bebida(bebidaSelecionada);
            p.setQuantidade_bebida(Integer.parseInt(txtQuantidadeBebida.getText()));
            p.setValor_bebida(p.getId_bebida().getValor());
            
            Sobremesa sobremesaSelecionada = (Sobremesa) cmbSobremesas.getSelectedItem();
            p.setId_sobremesa(sobremesaSelecionada);
            p.setQuantidade_sobremesa(Integer.parseInt(txtQuantidadeSobremesa.getText()));
            p.setValor_sobremesa(p.getId_sobremesa().getValor());
           
            
            Mesa mesaSelecionada = (Mesa) cmbMesa.getSelectedItem();
            p.setId_mesa(mesaSelecionada);
           
            
            
            
            PedidosDAO pDAO = new PedidosDAO();
            pDAO.inserir(p);
        
    }//GEN-LAST:event_btnEnviarParaCarrinhoActionPerformed

